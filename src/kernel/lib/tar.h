#pragma once

#include <stddef.h>

#include <lib/log.h>
#include <lib/string.h>

struct tar_header {
    char name[100];
    char mode[8];
    char uid[8];
    char gid[8];
    char size[12];
    char mtime[12];
    char chksum[8];
    char typeflag[1];
    char magic[6];
    char version[2];
    char uname[32];
    char gname[32];
    char devmajor[8];
    char devminor[8];
    char prefix[155];
} __attribute__((packed));

struct tar_block {
    char name[100];
    size_t size;
    void *data;
};

bool tar_read(void *tar, struct tar_block *block, size_t i);
