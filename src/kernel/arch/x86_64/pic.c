#include "pic.h"

/* Remap the PIC */
void remap_pic(void)
{
    outb(0x20, 0x11);
    outb(0xA0, 0x11);
    outb(0x21, 0x20);
    outb(0xA1, 0x28);
    outb(0x21, 0x04);
    outb(0xA1, 0x02);
    outb(0x21, 0x01);
    outb(0xA1, 0x01);
    outb(0x21, 0x0);
    outb(0xA1, 0x0);
}

void pic_disable(void)
{
    outb(0x21, 0xff);
    pic_wait();
    outb(0xA1, 0xff);
}
